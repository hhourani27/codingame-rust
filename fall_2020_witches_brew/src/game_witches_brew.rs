    #[test]
    fn test_valid_moves() {
        let mut stock = [3, 0, 0, 0];
        let mut empty_slots = 7;
        let mut spells = [
            Spell {
                id: 10,
                ingredients: [2, 0, 0, 0],
                delta_stock: 2,
                active: true,
            },
            Spell {
                id: 11,
                ingredients: [-1, 1, 0, 0],
                delta_stock: 0,
                active: true,
            },
            Spell {
                id: 12,
                ingredients: [0, -1, 1, 0],
                delta_stock: 0,
                active: true,
            },
            Spell {
                id: 13,
                ingredients: [0, 0, -1, 1],
                delta_stock: 0,
                active: true,
            },
        ];

        let orders: [Option<Order>; 5] = [
            Some(Order {
                id: 1,
                ingredients: [-2, 0, -3, 0],
                delta_stock: -5,
                price: 0,
            }),
            Some(Order {
                id: 2,
                ingredients: [-2, -2, 0, 0],
                delta_stock: -4,
                price: 0,
            }),
            Some(Order {
                id: 3,
                ingredients: [0, -3, 0, -2],
                delta_stock: -5,
                price: 0,
            }),
            Some(Order {
                id: 4,
                ingredients: [0, 0, 0, -5],
                delta_stock: -5,
                price: 0,
            }),
            Some(Order {
                id: 5,
                ingredients: [-2, 0, 0, -2],
                delta_stock: -4,
                price: 0,
            }),
        ];

        let expected_valid_moves = [Move::CAST(10), Move::CAST(11)];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [5, 0, 0, 0];
        empty_slots = 5;
        spells[0].active = false;

        let expected_valid_moves = [Move::CAST(11), Move::REST];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [4, 1, 0, 0];
        empty_slots = 5;
        spells[1].active = false;

        let expected_valid_moves = [Move::CAST(12), Move::REST];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [4, 0, 1, 0];
        empty_slots = 5;
        spells[2].active = false;

        let expected_valid_moves = [Move::CAST(13), Move::REST];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [4, 0, 0, 1];
        empty_slots = 5;
        spells[3].active = false;

        let expected_valid_moves = [Move::REST];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [4, 0, 0, 1];
        empty_slots = 5;
        for spell in spells.iter_mut() {
            spell.active = true;
        }

        let expected_valid_moves = [Move::CAST(10), Move::CAST(11)];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [5, 0, 0, 2];
        empty_slots = 3;
        for spell in spells.iter_mut() {
            spell.active = true;
        }

        let expected_valid_moves = [Move::CAST(10), Move::CAST(11), Move::BREW(5)];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [2, 2, 2, 2];
        empty_slots = 2;
        for spell in spells.iter_mut() {
            spell.active = true;
        }

        let expected_valid_moves = [
            Move::CAST(10),
            Move::CAST(11),
            Move::CAST(12),
            Move::CAST(13),
            Move::BREW(2),
            Move::BREW(5),
        ];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [2, 2, 2, 2];
        empty_slots = 2;
        spells[1].active = false;

        let expected_valid_moves = [
            Move::CAST(10),
            Move::CAST(12),
            Move::CAST(13),
            Move::BREW(2),
            Move::BREW(5),
            Move::REST,
        ];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [2, 4, 2, 2];
        empty_slots = 0;
        for spell in spells.iter_mut() {
            spell.active = true;
        }

        let expected_valid_moves = [
            Move::CAST(11),
            Move::CAST(12),
            Move::CAST(13),
            Move::BREW(2),
            Move::BREW(3),
            Move::BREW(5),
        ];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );

        /* */
        stock = [10, 0, 0, 0];
        empty_slots = 0;
        for spell in spells.iter_mut() {
            spell.active = false;
        }
        spells[0].active = true;

        let expected_valid_moves = [Move::REST];
        assert_vec_eq!(
            WitchesBrewGame::valid_moves(&orders, &spells, &stock, empty_slots,).get(),
            expected_valid_moves
        );
    }

