    #[test]
    fn test_can_fulfill_order() {
        let stock = [3, 2, 1, 0];

        let order = Order {
            id: 0,
            ingredients: [-1, -1, -1, 0],
            delta_stock: -3,
            price: 0,
        };
        assert!(WitchesBrewGame::can_fulfill_order(&order, &stock));

        let order = Order {
            id: 0,
            ingredients: [0, 0, -1, 0],
            delta_stock: -1,
            price: 0,
        };
        assert!(WitchesBrewGame::can_fulfill_order(&order, &stock));

        let order = Order {
            id: 0,
            ingredients: [-3, -2, -1, 0],
            delta_stock: -6,
            price: 0,
        };
        assert!(WitchesBrewGame::can_fulfill_order(&order, &stock));

        let order = Order {
            id: 0,
            ingredients: [-4, -2, -1, 0],
            delta_stock: -7,
            price: 0,
        };
        assert!(!WitchesBrewGame::can_fulfill_order(&order, &stock));

        let order = Order {
            id: 0,
            ingredients: [-3, -2, -2, 0],
            delta_stock: -7,
            price: 0,
        };
        assert!(!WitchesBrewGame::can_fulfill_order(&order, &stock));

        let order = Order {
            id: 0,
            ingredients: [0, 0, 0, -1],
            delta_stock: -1,
            price: 0,
        };
        assert!(!WitchesBrewGame::can_fulfill_order(&order, &stock));
    }

